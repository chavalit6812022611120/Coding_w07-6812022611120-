// ใช้ if-else if
#include <stdio.h> // นำเข้าไลบรารีสำหรับฟังก์ชันพื้นฐาน เช่น printf และ scanf

int main() { // จุดเริ่มต้นการทำงานของโปรแกรม
    
    int level; // ประกาศตัวแปรชื่อ level ชนิด integer (ตัวเลขจำนวนเต็ม) เพื่อเก็บระดับสมาชิก

    // --- ส่วนของการรับข้อมูล ---
    // แสดงข้อความบนหน้าจอเพื่อแนะนำผู้ใช้
    printf("กรุณาป้อนระดับสมาชิกของคุณ (1=Silver, 2=Gold, 3=Platinum, 4=Diamond): ");
    
    // รับค่าตัวเลขจากผู้ใช้แล้วเก็บไว้ในตัวแปร level
    scanf("%d", &level);

    printf("สิทธิประโยชน์ของคุณคือ: "); // แสดงข้อความเริ่มต้นก่อนบอกสิทธิประโยชน์

    // --- ส่วนของการตรวจสอบเงื่อนไข ---
    // ใช้ if-else if-else ในการตรวจสอบค่าของตัวแปร 'level'
    
    // เงื่อนไขที่ 1: ตรวจสอบว่า level มีค่าเท่ากับ 1 หรือไม่
    if (level == 1) {
        // ถ้าเงื่อนไขเป็นจริง (level คือ 1) จะทำงานในบล็อกนี้แล้วจบการตรวจสอบ
        printf("5%% discount\n");
    } 
    // เงื่อนไขที่ 2: ถ้าเงื่อนไขแรกไม่เป็นจริง จะมาตรวจสอบว่า level เท่ากับ 2 หรือไม่
    else if (level == 2) {
        // ถ้าเงื่อนไขนี้เป็นจริง จะทำงานในบล็อกนี้แล้วจบการตรวจสอบ
        printf("10%% discount + Reward points\n");
    } 
    // เงื่อนไขที่ 3: ถ้าเงื่อนไขข้างบนทั้งหมดไม่เป็นจริง จะมาตรวจสอบว่า level เท่ากับ 3 หรือไม่
    else if (level == 3) {
        // ถ้าเงื่อนไขนี้เป็นจริง จะทำงานในบล็อกนี้แล้วจบการตรวจสอบ
        printf("15%% discount + Reward points + Birthday gift\n");
    } 
    // เงื่อนไขที่ 4: ถ้าเงื่อนไขข้างบนทั้งหมดไม่เป็นจริง จะมาตรวจสอบว่า level เท่ากับ 4 หรือไม่
    else if (level == 4) {
        // ถ้าเงื่อนไขนี้เป็นจริง จะทำงานในบล็อกนี้แล้วจบการตรวจสอบ
        printf("ได้ทุกอย่าง + VIP events\n");
    } 
    // กรณีอื่นๆ: ถ้าไม่มีเงื่อนไขใดๆ ข้างบนเป็นจริงเลย
    else {
        // จะทำงานในบล็อกนี้ ซึ่งหมายความว่าผู้ใช้ป้อนเลขอื่นที่ไม่ใช่ 1, 2, 3, หรือ 4
        printf("ไม่พบระดับสมาชิกที่ระบุ\n");
    }

    return 0; // คืนค่า 0 เพื่อบอกว่าโปรแกรมทำงานเสร็จสิ้นสมบูรณ์
}